					<h3><u>Points Requirement</u></h3>
					<p>We require eligible initiates to fill out <a href="resources/Initiate_Points.pdf">a point verification form</a> over the course of their Initiation Cycle.
						<ul>
							<li>3 points must be from Social/House events (at least one individual house and one all-house competition)</li>
							<li>3 points must be from Academic/Professional events (including a <b>mandatory resumé critique</b>)</li>
							<li>3 points must be from Outreach/Community Service events</li>
							<li>6 points can be from any of the three categories listed</li>
						</ul>
						Points are usually given at a rate of 1 pt/hr at each event, and typically require a signature from a TBP officer as witness of your attendance. Your points sheet must be signed by at least 5 different officers to be considered complete. <b>The point verification form must be turned in on the day of Initiation. </b>
					</p>
